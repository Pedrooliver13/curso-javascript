/* FORMA COM O WHILE */
 var c = 1//ESSE É O INICIO
while(c <= 10){//ESSE É TESTE LÓGICO
    console.log(`Forma com While ${c}`)
    c++//ESSE É O INCREMENTO
} 
/* --------------------------------- */
/* Forma com o 'FOR' */
for (var d = 1; d <= 10;d++){//for(Inicio;Teste Lógico;Incremento)
    console.log(`teste ${d}`) //BLOCO A SER EXECUTADO(CASO O TESTE LÓGICO AINDA SEJA VERDADEIRO)
}
/* --------------------------------- */
