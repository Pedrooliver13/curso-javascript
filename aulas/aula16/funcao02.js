function soma(n1,n2){//FUNÇÃO SOMA TEM COMO PARAMETRO 'N1' E 'N2'
    return n1 + n2//RETORNA O RESULTADO DE ('N1' + 'N2') PARA A VARIAVEL/CHAMADA 
}
var soma = soma(5,3)//ESSA É A CHAMADA PARA A FUNÇÃO E UMA FORMA PARA DAR VALOR PARA 'N1' E 'N2'
console.log(`a SOMA é Igual a ${soma}`)//MANDO MOSTRA A QUE FOI DIGITADO!